"""06.-Escribir un programa en el cual se ingresen cuatro números, calcular e imprimir la resta de los dos primeros y 
la division del tercero y el cuarto."""

num1=int(input("Ingresa el primer numero"))
num2=int(input("Ingresa el segundo numero"))
num3=float(input("Ingresa el tercer numero"))
num4=float(input("Ingresa el cuatro numero"))

resta=num1-num2
division=num3/num4
print("La resta del primer y segundo numero es: ",resta)
